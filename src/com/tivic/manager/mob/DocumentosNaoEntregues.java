package com.tivic.manager.mob;

public class DocumentosNaoEntregues {
	
	private String nrAit;
	private String placa;
	private String dtInfracao;
	private int infracao;
	private String dsLocalInfracao;
	private String vlMultaDesconto;
	private String vlMulta;
	
	public String getNrAit() {
		return nrAit;
	}
	public void setNrAit(String nrAit) {
		this.nrAit = nrAit;
	}
	public String getPlaca() {
		return placa;
	}
	public void setPlaca(String placa) {
		this.placa = placa;
	}
	public String getDtInfracao() {
		return dtInfracao;
	}
	public void setDtInfracao(String dtInfracao) {
		this.dtInfracao = dtInfracao;
	}
	public int getInfracao() {
		return infracao;
	}
	public void setInfracao(int infracao) {
		this.infracao = infracao;
	}
	public String getDsLocalInfracao() {
		return dsLocalInfracao;
	}
	public void setDsLocalInfracao(String dsLocalInfracao) {
		this.dsLocalInfracao = dsLocalInfracao;
	}
	public String getVlMulta() {
		return vlMulta;
	}
	public void setVlMulta(String vlMulta) {
		this.vlMulta = vlMulta;
	}
	public String getVlMultaDesconto() {
		return vlMultaDesconto;
	}
	public void setVlMultaDesconto(String vlMultaDesconto) {
		this.vlMultaDesconto = vlMultaDesconto;
	}

}
