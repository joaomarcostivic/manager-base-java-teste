package com.tivic.manager.mob.acaousuario;

public class AcaoUsuarioDTO {

	private int cdAcao;
	private int cdUsuario;
	private String dsAcao;
	private long dtAcao;

	public int getCdAcao() {
		return cdAcao;
	}

	public void setCdAcao(int cdAcao) {
		this.cdAcao = cdAcao;
	}

	public int getCdUsuario() {
		return cdUsuario;
	}

	public void setCdUsuario(int cdUsuario) {
		this.cdUsuario = cdUsuario;
	}

	public String getDsAcao() {
		return dsAcao;
	}

	public void setDsAcao(String dsAcao) {
		this.dsAcao = dsAcao;
	}

	public long getDtAcao() {
		return dtAcao;
	}

	public void setDtAcao(long dtAcao) {
		this.dtAcao = dtAcao;
	}
}
